'use strict';

// config
process.env.API_PORT = 3000;
process.env.CORS_PREFLIGHT_ENABLED = true;
process.env.CORS_ALLOWED_ORIGIN = '*';
process.env.CORS_ALLOWED_METHODS = '*';
process.env.CORS_ALLOWED_HEADERS = 'Content-Type';
{{{config}}}

// deps
var express = require('express');
var app = express();
var setup = require(__dirname + '/setup');
var html = require(__dirname + '/html');
{{{deps}}}

// models
var models = {};
{{{models}}}

// middleware
app = setup.middlewares(app, express, models);
app = html.middlewares(app, express, models);
{{{middlewares}}}

// routes
app = html.routes(app, express, models);
{{{routes}}}

// start server
app.listen(process.env.API_PORT, function() {
	console.log(`API running on port ${process.env.API_PORT}`);
});


